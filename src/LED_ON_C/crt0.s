.text
.global _start
_start:
			ldr		sp,	=1024*4	@设置堆栈，注意：不能大于4k
							@nand flash中的代码在复位后会移到内部ram中，此ram只有4k
			bl		main		@调用C程序中的main函数
halt_loop:
			b		halt_loop
